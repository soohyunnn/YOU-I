CREATE TABLE `tb_file` (
	`FILE_SEQ` INT(11) NOT NULL AUTO_INCREMENT COMMENT '파일번호',
	`ORI_FILE_NM` VARCHAR(150) NOT NULL COMMENT '실제파일명',
	`SAVE_FILE_NM` VARCHAR(150) NOT NULL COMMENT '저장파일명',
	`THUMB_FILE_NM` VARCHAR(150) NULL DEFAULT NULL COMMENT '썸네일파일명',
	`UPLOAD_PATH` VARCHAR(150) NOT NULL COMMENT '저장경로',
	`REF_TYPE` VARCHAR(30) NOT NULL COMMENT '참조유형',
	`REF_CD` VARCHAR(200) NOT NULL COMMENT '참조코드',
	`REG_ID` VARCHAR(50) NULL DEFAULT NULL COMMENT '등록자',
	`REG_DT` VARCHAR(14) NULL DEFAULT NULL COMMENT '등록일시',
	`MOD_ID` VARCHAR(50) NULL DEFAULT NULL COMMENT '수정자',
	`MOD_DT` VARCHAR(14) NULL DEFAULT NULL COMMENT '수정일시',
	PRIMARY KEY (`FILE_SEQ`)
)
COMMENT='파일'
COLLATE='utf8_general_ci'
ENGINE=InnoDB
;
